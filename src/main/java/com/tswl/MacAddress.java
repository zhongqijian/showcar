package com.tswl;

public class MacAddress {
	private String mac;
	private String user;
	//方法和返回值
	public String getMac() {
		return mac;
	}
	public void setMac(String mac) {
		this.mac = mac;
	}
	public String getUser() {
		return user;
	}
	public void setUser(String user) {
		this.user = user;
	}
}